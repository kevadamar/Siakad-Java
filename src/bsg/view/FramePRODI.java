    private void getDataTable() {
        model = (DefaultTableModel) table.getModel();
        try {
            Statement stat = connection.createStatement();
            String sql = "SELECT * FROM prodi";
            ResultSet res = stat.executeQuery(sql);
            while(res.next ()) {
                Object[ ] obj = new Object[2];
                obj[0] = res.getString("id_prodi");
                obj[1] = res.getString("nama_prodi");
                model.addRow(obj);
            }
        } catch(SQLException err) {
            err.printStackTrace();
        }
    }
    
    private void refresh() {
        model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        getDataTable();
        tIDProdi.setEditable(true);
    }
    
    private void reset() {
        tIDProdi.setText("");
        tIDProdi.setEditable(true);
        tNama.setText("");
        bSimpan.setEnabled(true);
        bUbah.setEnabled(false);
        bHapus.setEnabled(false);
    }
    
    private void insert() {
        PreparedStatement statement = null;
        String sql = "INSERT INTO prodi (id_prodi,nama_prodi) VALUE(?,?);";
        try {
            statement = connection.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
            statement.setString(1, tIDProdi.getText());
            statement.setString(2, tNama.getText());
            statement.executeUpdate();
        } catch (SQLException ex) {
            ex.printStackTrace();
        } finally {
            try {
                statement.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
    
    private void update() {
        PreparedStatement statement = null;
        String sql = "UPDATE prodi SET nama_prodi=? WHERE id_prodi=?";
        try {
            statement = connection.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
            statement.setString(1, tNama.getText());
            statement.setString(2, tIDProdi.getText());
            statement.executeUpdate();
        } catch (SQLException ex) {
            ex.printStackTrace();
        } finally {
            try {
                statement.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
    
    private void delete() {
        PreparedStatement statement = null;
        String sql = "DELETE FROM prodi WHERE id_prodi=?";
        try {
            statement = connection.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
            statement.setString(1, tIDProdi.getText());
            statement.executeUpdate();
        } catch (SQLException ex) {
            ex.printStackTrace();
        } finally {
            try {
                statement.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
    
    private void search() {
        model = (DefaultTableModel) table.getModel();
        PreparedStatement statement = null;
        try {
            String sql = "SELECT * FROM prodi WHERE nama_prodi LIKE ?";
            statement = connection.prepareStatement(sql);
            statement.setString(1, "%" + tCari.getText() + "%");
            ResultSet res = statement.executeQuery();
            while (res.next ()) {
                Object[ ] obj = new Object[2];
                obj[0] = res.getString("id_prodi");
                obj[1] = res.getString("nama_prodi");
                model.addRow(obj);
        }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

